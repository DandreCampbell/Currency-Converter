public class Currency
{
  private double rate;
  private String base;
  private String date;

  public void setRate(double rate) {
    this.rate = rate;
  }

  public double getRate() {
    return rate;
  }

  public void setBase(String base) {
    this.base = base;
  }

  public String getBase() {
    return base;
  }

  public void setDate(String date) {
    this.date = date;
  }

  public String getDate() {
    return date;
  }
}
